                    else if (maze[i, j] == '1')
                        paintRoad(maze[i, j]);
                    else
                        paintWall(maze[i, j]);
                }
                Console.WriteLine();
            }
        }

        static void road(char[,] maze, int x, int y, int xEnd, int yEnd, int[] numberSteps, int numberRoads)
        {
            if (x == xEnd && y == yEnd)
            {
                Console.WriteLine("llego al fin");
                printMaze(maze);
            }
            else
            {
                for (int i = 0; i < 4; i++)
                {
                    switch (i)
                    {
                        case 0:
                            if (isSafe(maze, x, y + 1))
                            {
                                maze[x, y + 1] = '1';
                                road(maze, x, y + 1, xEnd, yEnd, numberSteps, numberRoads);
                                maze[x, y + 1] = '0';
                            }
                            break;
                        case 1:
                            if (isSafe(maze, x + 1, y))
                            {
                                maze[x + 1, y] = '1';
                                road(maze, x + 1, y, xEnd, yEnd, numberSteps, numberRoads);
                                maze[x + 1, y] = '0';
                            }
                            break;

                        case 2:
                            if (isSafe(maze, x, y - 1))
                            {
                                maze[x, y - 1] = '1';
                                road(maze, x, y - 1, xEnd, yEnd, numberSteps, numberRoads);
                                maze[x, y - 1] = '0';
                            }
                            break;
                        case 3:
                            if (isSafe(maze, x - 1, y))
                            {
                                maze[x - 1, y] = '1';
                                road(maze, x - 1, y, xEnd, yEnd, numberSteps, numberRoads);
                                maze[x - 1, y] = '0';
                            }
                            break;
                    }
                }
            }
        }



