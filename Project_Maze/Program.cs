        static bool isSafe(char[,] maze, int x, int y)
        {
            if (x >= 1 && y >= 1 && x <= 30 && y <= 30 && maze[x, y] == '0')
                return true;
            return false;
        }

        static void printMaze(char[,] maze)
        {
            for (int i = 0; i < 32; i++)
            {
                for (int j = 0; j < 32; j++)
                {
                    //Console.SetCursorPosition(20 + j, 1 + i);
                    if (maze[i, j] == '║' || maze[i, j] == '╚' || maze[i, j] == '╔' || maze[i, j] == '╝' || maze[i, j] == '═' || maze[i, j] == '╗')
